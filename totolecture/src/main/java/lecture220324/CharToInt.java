package lecture220324;

public class CharToInt {
    public static void main(String[] args) {
        char c1='a';
        System.out.println((int)c1);
        int a=97;
        System.out.println((char)a);
        System.out.println((char)(a+1));
    }
}//이진법은 숫자만 나타냄. 유니코드는 숫자에 해당하는 문자가 미리 정해져 있음(약속).
